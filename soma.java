import java.util.*;  

public class soma {
    public static void main(String[] args) {
        
        Scanner sc= new Scanner(System.in);  
        
        System.out.print("Digite o primeiro número: ");  
        int a= sc.nextInt();  
        
        System.out.print("Digite o segundo número: ");  
        int b= sc.nextInt();  
        
        int c = a + b;
        
        System.out.println("Total da soma é " + c + ".\n");  
    }  
}  